# -Basic printing
print("Hello-World")
print("Aiza")
# -Multiple Items
print("Aiza","Adeel")
print ("numbers:","n=1","n=2","n=3")
# -Special Characters
print("Hello \nWorld") 
print("Hello \tWorld")
# -Sep Parameter
print ("Cricket", "Football", "hockey", sep=", ")
print(1,2,3,sep="-")
# -End Parameter
print("Hello",end=" ")
print("World")
print(1,end="")
print(2)
# -Escape Parameter
print('She said','"hello" to me.')
print("Hello\\")
print("She said","\"hello\" to me")
# -Combining Text and Numbers
print("I am a graphic designer.", "I am",22,"years old")
print("I am",22,"years old")
# -Loop Printing
for n in range(1,6):
    print(n)